@login_required
@user_passes_test(is_admin, login_url='/login/')
def approve_credit_card(request, app_id):
    """Approve a credit card application, create the account, and notify the user."""
    if request.method != 'POST':
        return redirect('admin_credit_card_list')

    application = get_object_or_404(CreditCardApplication, id=app_id, status='PENDING')
    assigned_type = request.POST.get('assigned_card_type', 'SILVER').upper()
    credit_limit_str = request.POST.get('credit_limit', '0').replace(',', '')
    admin_remarks = request.POST.get('admin_remarks', '')

    try:
        credit_limit = float(credit_limit_str)
    except (ValueError, TypeError):
        messages.error(request, 'Invalid credit limit value.')
        return redirect('admin_credit_card_list')

    with transaction.atomic():
        # Update application status
        application.status = 'APPROVED'
        application.assigned_card_type = assigned_type
        application.credit_limit = credit_limit
        application.admin_remarks = admin_remarks
        application.save()

        # Generate a unique credit card account number  
        import random
        while True:
            card_acc_num = f"CC{''.join(random.choices('0123456789', k=10))}"
            if not Account.objects.filter(account_number=card_acc_num).exists():
                break

        # Create the credit card account for the user with credit_limit as balance
        Account.objects.create(
            user=application.user,
            account_number=card_acc_num,
            account_type='CREDIT',
            balance=credit_limit,
            card_type=assigned_type,
            credit_limit=credit_limit,
        )

        card_labels = {'PLATINUM': 'Platinum', 'GOLD': 'Gold', 'SILVER': 'Silver'}
        card_label = card_labels.get(assigned_type, assigned_type.capitalize())

        # Notify the user
        Notification.objects.create(
            user=application.user,
            title=f"🎉 Your {card_label} Credit Card is Approved!",
            message=(
                f"Congratulations {application.user.first_name or application.user.username}! "
                f"Your application for a {card_label} Credit Card has been approved.\n\n"
                f"Card Number: {card_acc_num}\n"
                f"Credit Limit: ₹{credit_limit:,.0f}\n"
                f"Card Type: {card_label}\n\n"
                + (f"Admin Remarks: {admin_remarks}" if admin_remarks else "You can now use your credit card for purchases.")
            ),
            notification_type='CREDIT_CARD'
        )

    messages.success(request, f"{card_label} Card approved for {application.user.username}. Account {card_acc_num} created with ₹{credit_limit:,.0f} limit.")
    return redirect('admin_credit_card_list')


