@login_required
@user_passes_test(is_admin, login_url='/login/')
def admin_add_transaction(request):
    """Admin endpoint to forcefully manually create a transaction."""
    if request.method == 'POST':
        account_number = request.POST.get('account_number')
        amount_str = request.POST.get('amount')
        transaction_type = request.POST.get('transaction_type')
        description = request.POST.get('description', 'Admin Manual Override')

        try:
            amount = Decimal(amount_str)
            if amount <= Decimal('0'):
                raise InvalidOperation
        except (InvalidOperation, TypeError):
            messages.error(request, 'Invalid amount provided.')
            return redirect('admin_transactions_list')

        account = Account.objects.filter(account_number=account_number).first()
        if not account:
            messages.error(request, f'Account number {account_number} not found.')
            return redirect('admin_transactions_list')

        try:
            with transaction.atomic():
                if transaction_type == 'CREDIT':
                    account.balance = account.balance + amount
                elif transaction_type == 'DEBIT':
                    if account.balance < amount:
                        messages.warning(request, f'Warning: Balance will go negative after this debit.')
                    account.balance = account.balance - amount
                else:
                    messages.error(request, 'Invalid transaction type.')
                    return redirect('admin_transactions_list')
                
                account.save()
                
                # Log transaction
                Transaction.objects.create(
                    account=account,
                    amount=amount,
                    transaction_type=transaction_type,
                    status='APPROVED',
                    description=f"[ADMIN_OVERRIDE] {description}"
                )
                
            messages.success(request, f'Successfully applied {transaction_type} of ₹{amount:,.2f} to {account_number}.')
        except Exception as e:
            messages.error(request, f'Error creating transaction: {str(e)}')

    # Check for a next URL in query or referral header
    next_url = request.GET.get('next') or request.META.get('HTTP_REFERER')
    return redirect(next_url if next_url else 'admin_transactions_list')


