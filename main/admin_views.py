@login_required
@user_passes_test(is_admin, login_url='/login/')
def admin_add_user(request):
    """Manually onboard a new customer."""
    if request.method == 'POST':
        u = request.POST.get('username')
        e = request.POST.get('email')
        fn = request.POST.get('first_name', '')
        ln = request.POST.get('last_name', '')
        p = request.POST.get('password')
        pan = request.POST.get('pan_number', '')

        if User.objects.filter(username=u).exists():
            messages.error(request, f"Username {u} already exists.")
            return redirect('admin_users_list')

        try:
            with transaction.atomic():
                user = User.objects.create_user(username=u, email=e, password=p, first_name=fn, last_name=ln)
                # Check if profile already created via signals
                profile, created = CustomerProfile.objects.get_or_create(user=user)
                profile.pan_number = pan
                profile.is_approved = True
                profile.save()
                
                # Create a basic account for them
                acc_num = f"1000{user.id:08d}"
                Account.objects.get_or_create(user=user, defaults={'account_number': acc_num})
                
            messages.success(request, f"User {u} onboarded successfully.")
        except Exception as e:
            messages.error(request, f"Error: {str(e)}")
            
    return redirect('admin_users_list')

