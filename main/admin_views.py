@login_required
@user_passes_test(is_admin, login_url='/login/')
def approve_transaction(request, transaction_id):
    txn = get_object_or_404(Transaction, id=transaction_id)
    if txn.status == 'PENDING':
        with transaction.atomic():
            txn.status = 'APPROVED'
            txn.save()

            # Logic for different transaction types
            if txn.transaction_type == 'CREDIT':
                # This was a pending deposit
                txn.account.balance += txn.amount
                txn.account.save()
            
            elif txn.transaction_type == 'DEBIT' and txn.receiver_account:
                # This was a pending transfer
                # Sender balance was already deducted in the initial view
                txn.receiver_account.balance += txn.amount
                txn.receiver_account.save()
                
                # Create a credit transaction for the receiver
                Transaction.objects.create(
                    account=txn.receiver_account,
                    description=f"Transfer from A/C {txn.account.account_number}",
                    amount=txn.amount,
                    transaction_type='CREDIT',
                    status='APPROVED'
                )
            
        messages.success(request, f"Transaction {txn.id} approved.")
    return redirect('admin_dashboard')

@login_required
@user_passes_test(is_admin, login_url='/login/')
def reject_transaction(request, transaction_id):
    txn = get_object_or_404(Transaction, id=transaction_id)
    if txn.status == 'PENDING':
        with transaction.atomic():
            # If it was a debit, we need to refund the account because it was already deducted in the initial view
            if txn.transaction_type == 'DEBIT':
                txn.account.balance += txn.amount
                txn.account.save()
            txn.status = 'REJECTED'
            txn.save()
            messages.warning(request, f"Transaction {txn.id} rejected and funds reverted.")
    return redirect('admin_dashboard')

