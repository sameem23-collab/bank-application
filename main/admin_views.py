@login_required
@user_passes_test(is_admin, login_url='/login/')
def admin_add_account(request):
    """Manually create a bank account for a user."""
    if request.method == 'POST':
        username = request.POST.get('username', '').strip()
        account_type = request.POST.get('account_type', 'SAVINGS')
        initial_balance_str = request.POST.get('initial_balance', '0')

        target_user = User.objects.filter(username=username).first()
        if not target_user:
            messages.error(request, f'No user found with username "{username}".')
            return redirect('admin_accounts_list')

        try:
            initial_balance = Decimal(initial_balance_str)
            if initial_balance < Decimal('0'):
                raise InvalidOperation
        except InvalidOperation:
            messages.error(request, 'Invalid initial balance.')
            return redirect('admin_accounts_list')

        try:
            with transaction.atomic():
                import random
                while True:
                    acc_num = ''.join(random.choices('0123456789', k=10))
                    if not Account.objects.filter(account_number=acc_num).exists():
                        break

                account = Account.objects.create(
                    user=target_user,
                    account_number=acc_num,
                    account_type=account_type,
                    balance=initial_balance,
                )

                if initial_balance > Decimal('0'):
                    Transaction.objects.create(
                        account=account,
                        amount=initial_balance,
                        transaction_type='CREDIT',
                        status='APPROVED',
                        description='[ADMIN_OVERRIDE] Initial account funding'
                    )

            messages.success(request, f'Account {acc_num} created for {username} with balance ₹{initial_balance:,.2f}.')
        except Exception as ex:
            messages.error(request, f'Error creating account: {str(ex)}')

    return redirect('admin_accounts_list')


