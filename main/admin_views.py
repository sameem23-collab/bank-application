@login_required
@user_passes_test(is_admin, login_url='/login/')
def approve_loan(request, app_id):
    """Approve a loan application and notify the user."""
    if request.method != 'POST':
        return redirect('admin_loan_list')

    application = get_object_or_404(LoanApplication, id=app_id, status='PENDING')
    admin_remarks = request.POST.get('admin_remarks', 'Your loan application meets our criteria.')

    with transaction.atomic():
        # Update application status
        application.status = 'APPROVED'
        application.admin_remarks = admin_remarks
        application.save()

        # Logic: Credit the loan amount to the user's primary savings account
        primary_account = Account.objects.filter(user=application.user, account_type='SAVINGS').first()
        if primary_account:
            primary_account.balance += application.amount
            primary_account.save()

            # Create a transaction record
            Transaction.objects.create(
                account=primary_account,
                description=f"Loan Disbursement: {application.get_loan_type_display()}",
                amount=application.amount,
                transaction_type='CREDIT',
                status='APPROVED'
            )

        # Notify the user
        Notification.objects.create(
            user=application.user,
            title=f"✅ Your {application.get_loan_type_display()} is Approved!",
            message=(
                f"Congratulations {application.user.first_name or application.user.username}! "
                f"Your request for a {application.get_loan_type_display()} of ₹{application.amount:,.2f} has been approved.\n\n"
                f"The amount has been credited to your primary account.\n"
                f"Tenure: {application.tenure_years} Years\n\n"
                + (f"Admin Remarks: {admin_remarks}" if admin_remarks else "")
            ),
            notification_type='SYSTEM'
        )

    messages.success(request, f"{application.get_loan_type_display()} approved for {application.user.username}. ₹{application.amount:,.2f} credited.")
    return redirect('admin_loan_list')


